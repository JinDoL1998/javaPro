public class Ex06_03 {

	public static void main(String[] args) {
		
		//				0000 1010
		System.out.println(10);
		System.out.println(10 >> 2);	// 2
		System.out.println(10 >>> 2);	// 2
		System.out.println(10 << 2);	// 40
		
		
	}

}
