public class Ex02 {

	// String name; 지역변수 x
	
	public static void main(String[] args) {
		// 지역변수(local variable)는 반드시 초기화해야 사용할 수 있다.
		// The value of the local variable name is not used
		String name; // 지역변수 o
		
		name = "김진성";
		
		// The local variable name may not have been initialized
		System.out.println( name );
		
		
		
	}

}
